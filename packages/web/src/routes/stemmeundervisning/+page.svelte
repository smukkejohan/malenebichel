        <h1 class="text-xl"> Stemmeundervisning</h1>
        <p>Stemmen er klangen af en vibration formet af følelser, mentale kræfter, muskelarbejde, attitude, energi og meget andet.
Stemmen kan der ikke være noget galt med. Den er jo lige netop lyden af dig - som du er og som du har det - lige nu.
Fordi stemmen er det autentiske udtryk for vores følelser, fordomme, begær, indsigter og kropslige formåen, så er det også en sikker indikator for de kropslige og mentale blokeringer, der måtte findes. 
Utrænede ører registrerer at der er noget galt, eller at man ikke kan li' stemmen, mens trænede ører kan henføre en bestemt klang til en konkret blokering af flow'et i krop eller følelser. </p>
<p>Arbejdet med stemme og krop med fokus på mindfulness, nærvær og kreativitet kan derfor være indfaldsvinkel til mange forskellige problematikker og udviklingsmål.
(bl. a. hengivelse/sensualitet, gennemslagskraft, kunstnerisk eller personlig afklaring, - problemer med at synge rent, - med at blive hørt, - med at lytte, fysiske spændinger i tale og sang, stress, udbrændthed, præstationsangst)</p>
<p>Kontakt mig gerne på malenebi@gmail.com, hvis du ønsker hjælp, inspiration, coaching eller rådgivning som ikke dækkes af de aktuelle workshops. Jeg tilrettelægger så et forløb for en eller flere personer, ud fra det aktuelle behov.</p>
