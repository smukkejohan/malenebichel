<p><strong>Som proceskonsulent</strong> arbejder jeg ofte med teamsamarbejde. - og hjælper teams til at kommunikere mere autentisk og derfor mere kreativt. -men faciliterer også større processer og møder for skoler og andre virksomheder. For tiden er jeg musikfaglig konsulent for Vallensbæk Kommunes projekt "Den Musikalske Fødekædestrategi"</p>
<p><strong>Som lærer</strong> 
-Er jeg studielektor i musikpædagogik på Rytmisk Musikkonservatorium, ekstern stemmekonsulent for forskere og undervisere på IT-Universitetet, samt </p>
<p>Desuden arbejder jeg med sammenhængen mellem nærvær og kreativitet med udgangspunkt i stemmearbejde.
Jeg hjælper mennesker med at finde deres autentiske udtryk og kreativitet gennem arbejde med kontakt til stemmen og indlevelse i det stof, man gerne vil videregive.
Jeg underviser og superviserer konsulenter, coaches, vejledere og undervisere, der ønsker at have mere fokus på stemme og krop i deres praksis.</p>
<p>Jeg er uddannet sanger og musikpædagog på musikkonservatoriet og som master i læreprocesser ved Aalborg Universitet og har desuden taget en uddannelse som proceskonsulent 
For tiden er jeg studerende på kandidatuddannelsen i musikdidaktik på DPU. </p>
<p>Jeg har dyrket yoga og meditation siden 1986
- Siden 2008 har jeg dyrket meditation, tantra og energibevidsthed under supervision af Anne Sophie Jørgensen.</p>
<p>Er formand for Dansk Vokalforening siden maj 2010
med-iværksætter af Global Units, - et netværk for kunstnere og mødested for improviserede livestreamede koncerter på tværs af genrer og kunstarter.
Medskaber af "inspirationsnetværk om nye musikalske læringsmiljøer for børn og unge", der er støttet af Statens Kunstfond
Desuden har jeg arbejdet som frivillig sanglærer for flygtningekvinder i Røde Kors.</p>
<p>Jeg har mere end 10 år undervist i retorik og musik på seminarier og i sang på universitetet, samt været konsulent i skoler, institutioner og virksomheder.</p>
<p>Jeg har således hjulpet bl.a folkeskolelærere, pædagoger, sangere og musikstuderende med:
- at lade sig forføre og røre så meget af stoffet, at tilhørere og elever smittes af det. 
- at bevare den tantriske nysgerrighed overfor verden, -så det at være undersøgende, anerkendende og legende er den vigtigste del af at være rollemodel som underviser eller kunstner.</p>
