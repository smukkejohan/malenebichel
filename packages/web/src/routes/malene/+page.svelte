    <h1>Om Malene</h1>
    <p><strong>Som sanger</strong> arbejder jeg især med moderne kompositionsmusik og improvisation<br />
</p>
<p>I ensemblet Clara sammen med Birgitte Lindum, obo og Berit Spælling, harpe.<br />
</p>
<p>I duo med Dean Delgiusto, Akkordeon</p>
<p>I duoen RIMA med Marie Falk, cello</p>
<p>I duo med Mikke Andersen, guitar</p>
<p>Til meditationsgudstjenester i Sions Kirke, Østerbro</p>
<p>og i improvisationskvartetten, Open Form Ensemble med Dean Delgiusto, Carl Bergstrøm Nielsen og Marie Wärme</p>
<p>Som lydskaber har jeg i en årrække ledet vokaleksperimentariet, Klangsaft og har gennem huskunsterordningen  komponeret sammen med børn for deres stemmer i en række projekter.
Tidligere har jeg arbejdet en del sammen med nu afdøde komponist og lydudvikler Knud Riishøjgaard, </p>
