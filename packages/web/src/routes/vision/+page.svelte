<h2>Stemmen</h2>
<p>Stemmen er lyden af en proces..<br />
Ligesom torden er lyden af lyn<br />
at bruge stemme er at udtrykke sin proces i lyd <br />
At bruge stemme er at skabe med lyd<br />
At bruge stemme er at skabe i lyd<br />
</p>
<p>At arbejde med stemmen <br />
er en meget håndgribelig og tilgængelig indfaldsvinkel <br />
i arbejdet med nærvær.<br />
- Og forøget nærvær åbner direkte kontakt <br />
til stemmen og dens muligheder.<br />
</p>
