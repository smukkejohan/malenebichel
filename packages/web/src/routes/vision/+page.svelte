        <h2>Processen</h2>
<p>Egentlig er det enkelt.<br />
Der findes ikke andet end proces.<br />
Livet er proces<br />
Jeg er proces<br />
Du er proces<br />
Kemisk, biologisk, psykisk, energetisk, fysisk......<br />
På celleplan, kropsplan, relationsplan, samfundsplan.........<br />
</p>
<p>Nærvær i "nuet" er nærvær i processen<br />
bevidsthed er bevidsthed i processen <br />
og bevidsthed om processen.<br />
</p>
<p>Forandring og forvandling er altså det allermest basale<br />
-og det vi finder når vi mærker efter i os selv.<br />
</p>
<p>Jeg er optaget af at nærme mig denne enkelhed <br />
i al væren og gøren<br />
at anerkende, rumme og være i de processer <br />
som er mig<br />
at anerkende, rumme og være i de processer <br />
som er os <br />
at anerkende, rumme og være i de processer <br />
som er livet<br />
at anerkende, rumme og være i de processer <br />
som udgør kreativitet <br />
på alle individuelle og kollektive planer. <br />
</p>
<p>At gøre mig mere og mere fri af omsvøb<br />
og nærme mig den transparens,<br />
som gør min oplevelse,<br />
min kommunikation med andre<br />
og min performance<br />
autentisk.<br />
Uden omsvøb<br />
</p>
